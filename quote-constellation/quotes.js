// LocalStorage keys
const STORAGE_KEY = 'quote-constellation-quotes';
const EMBEDDINGS_KEY = 'quote-constellation-embeddings';

// Generate a simple UUID
function generateId() {
  return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function(c) {
    const r = Math.random() * 16 | 0;
    const v = c === 'x' ? r : (r & 0x3 | 0x8);
    return v.toString(16);
  });
}

// Load quotes from localStorage or return seed quotes
export function loadQuotes() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      return JSON.parse(stored);
    }
  } catch (e) {
    console.warn('Failed to load quotes from localStorage:', e);
  }

  // Initialize with seed quotes
  const initialQuotes = seedQuotes.map((q, index) => ({
    id: `seed-${index}`,
    text: q.text,
    attribution: q.attribution,
    userAdded: false
  }));

  saveQuotes(initialQuotes);
  return initialQuotes;
}

// Save quotes to localStorage
export function saveQuotes(quotes) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(quotes));
  } catch (e) {
    console.warn('Failed to save quotes to localStorage:', e);
  }
}

// Add a new quote
export function addQuote(text, attribution = null) {
  const quotes = loadQuotes();
  const newQuote = {
    id: generateId(),
    text: text.trim(),
    attribution: attribution?.trim() || null,
    userAdded: true
  };

  quotes.push(newQuote);
  saveQuotes(quotes);

  return newQuote;
}

// Load embeddings from localStorage
export function loadEmbeddings() {
  try {
    const stored = localStorage.getItem(EMBEDDINGS_KEY);
    if (stored) {
      return JSON.parse(stored);
    }
  } catch (e) {
    console.warn('Failed to load embeddings from localStorage:', e);
  }
  return {};
}

// Save embeddings to localStorage
export function saveEmbeddings(embeddings) {
  try {
    localStorage.setItem(EMBEDDINGS_KEY, JSON.stringify(embeddings));
  } catch (e) {
    console.warn('Failed to save embeddings to localStorage:', e);
  }
}

// Get embedding for a specific quote
export function getEmbedding(quoteId) {
  const embeddings = loadEmbeddings();
  return embeddings[quoteId] || null;
}

// Set embedding for a specific quote
export function setEmbedding(quoteId, embedding) {
  const embeddings = loadEmbeddings();
  embeddings[quoteId] = embedding;
  saveEmbeddings(embeddings);
}
